'use client'

import React from 'react'
import { useTerminal } from './TerminalProvider'

export function TerminalToggle() {
  const { state, toggleFullScreen, toggleVisibility } = useTerminal()

  return (
    <div className="fixed bottom-4 right-4 z-50 flex flex-col gap-2">
      {/* Toggle fullscreen button */}
      <button
        onClick={toggleFullScreen}
        className="p-3 rounded-full bg-neutral-800 dark:bg-neutral-700 text-white shadow-lg hover:bg-neutral-700 dark:hover:bg-neutral-600 transition-colors"
        title={state.isFullScreen ? 'Exit fullscreen (T)' : 'Fullscreen terminal (T)'}
        aria-label={state.isFullScreen ? 'Exit fullscreen' : 'Fullscreen terminal'}
      >
        {state.isFullScreen ? (
          // Minimize icon
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            strokeWidth={1.5}
            stroke="currentColor"
            className="w-5 h-5"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M9 9V4.5M9 9H4.5M9 9 3.75 3.75M9 15v4.5M9 15H4.5M9 15l-5.25 5.25M15 9h4.5M15 9V4.5M15 9l5.25-5.25M15 15h4.5M15 15v4.5m0-4.5 5.25 5.25"
            />
          </svg>
        ) : (
          // Maximize icon
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            strokeWidth={1.5}
            stroke="currentColor"
            className="w-5 h-5"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M3.75 3.75v4.5m0-4.5h4.5m-4.5 0L9 9M3.75 20.25v-4.5m0 4.5h4.5m-4.5 0L9 15M20.25 3.75h-4.5m4.5 0v4.5m0-4.5L15 9m5.25 11.25h-4.5m4.5 0v-4.5m0 4.5L15 15"
            />
          </svg>
        )}
      </button>

      {/* Toggle visibility button (mobile only in non-fullscreen) */}
      <button
        onClick={toggleVisibility}
        className="p-3 rounded-full bg-neutral-800 dark:bg-neutral-700 text-white shadow-lg hover:bg-neutral-700 dark:hover:bg-neutral-600 transition-colors md:hidden"
        title={state.isVisible ? 'Hide terminal' : 'Show terminal'}
        aria-label={state.isVisible ? 'Hide terminal' : 'Show terminal'}
      >
        {state.isVisible ? (
          // Eye off icon
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            strokeWidth={1.5}
            stroke="currentColor"
            className="w-5 h-5"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M3.98 8.223A10.477 10.477 0 0 0 1.934 12C3.226 16.338 7.244 19.5 12 19.5c.993 0 1.953-.138 2.863-.395M6.228 6.228A10.451 10.451 0 0 1 12 4.5c4.756 0 8.773 3.162 10.065 7.498a10.522 10.522 0 0 1-4.293 5.774M6.228 6.228 3 3m3.228 3.228 3.65 3.65m7.894 7.894L21 21m-3.228-3.228-3.65-3.65m0 0a3 3 0 1 0-4.243-4.243m4.242 4.242L9.88 9.88"
            />
          </svg>
        ) : (
          // Eye icon
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            strokeWidth={1.5}
            stroke="currentColor"
            className="w-5 h-5"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M2.036 12.322a1.012 1.012 0 0 1 0-.639C3.423 7.51 7.36 4.5 12 4.5c4.638 0 8.573 3.007 9.963 7.178.07.207.07.431 0 .639C20.577 16.49 16.64 19.5 12 19.5c-4.638 0-8.573-3.007-9.963-7.178Z"
            />
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              d="M15 12a3 3 0 1 1-6 0 3 3 0 0 1 6 0Z"
            />
          </svg>
        )}
      </button>

      {/* Keyboard shortcut hint */}
      <div className="hidden md:block text-xs text-muted text-center opacity-50">
        Press T
      </div>
    </div>
  )
}
